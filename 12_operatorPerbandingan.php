<?php
echo "Operator Perbandingan\n";
echo "Sama dengan (==)\n";
$a = 5;
$b = 5;
echo "5 == 5 => ";
var_dump($a==$b);

echo "Lebih Besar (>)\n";
$a = 5;
$b = 4;
echo "5 > 4 => ";
var_dump($a>$b);

echo "Lebih Kecil (<)\n";
$a = 5;
$b = 4;
echo "5 < 4 => ";
var_dump($a<$b);

echo "Lebih Besar atau sama dengan (>=)\n";
$a = 5;
$b = 5;
echo "5 >= 5 => ";
var_dump($a>=$b);